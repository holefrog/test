package Unit3;

public class Degrees {
	private int number;
	float percent;
	private String name;
	
	public String getName() {
	    return this.name;
	}
	
	public void setName(String name) {
	    this.name = name;
	}

	public int getNumber() {
	    return this.number;
	}
	
	public void setNumber(int num) {
	    this.number = num;
	}

	public float getPercent() {
	    return this.percent;
	}
	
	public void setPercent(float num) {
	    this.percent = num;
	}

}
